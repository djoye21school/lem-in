static	int		ft_skip(int i)
{
	if (i == ' ' || i == '\t' || i == '\n' || i == '\r' || i == '\v'
		|| i == '\f')
		return (1);
	return (0);
}

int				ft_atoi(const char *str)
{
	int					i;
	int					sign;
	unsigned long int	nb;

	i = 0;
	sign = 1;
	nb = 0;
	while (ft_skip(str[i]) == 1 && str[i])
		i++;
	if (str[i] == '-')
		sign = -1;
	if ((str[i] == '-') || (str[i] == '+'))
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (((nb > 922337203685477580) || (nb == 922337203685477580
										   && ((str[i] - '0') > 7))) && sign == 1)
			return (-1);
		else if (((nb > 922337203685477580) || (nb == 922337203685477580
												&& ((str[i] - '0') > 8))) && sign == -1)
			return (0);
		nb = (nb * 10) + (str[i] - '0');
		i++;
	}
	return ((int)(nb * sign));
}
